//Add numbers
function add(num1, num2) {
    return num1 + num2;
}

console.log("Adding " + add(1, 1));

//Subtract numbers
function subtract(num1, num2) {
    return num1 - num2;
}

console.log("Subtracting " +subtract(1,-1));


//Divide Numbers
function divide(num1, num2) {
    return num1 / num2;
}

console.log("Dividing " + divide(1, 1));

//Multiply Numbers
function multiply(num1, num2) {
    return num1 * num2;
}

console.log("Multiplying " + multiply(4, 4));

//Raise to power
function exponent(num1, num2) {
    return num1 ** num2;
}

console.log ("Exponent " + exponent(3,3));